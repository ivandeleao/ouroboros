package model.nosql;

import java.time.LocalDate;

/**
 *
 * @author ivand
 */
public class LogAtualizacaoItem {

    LocalDate data;
    String descricao;

    public LogAtualizacaoItem(LocalDate data, String descricao) {
        this.data = data;
        this.descricao = descricao;
    }

    public LocalDate getData() {
        return data;
    }

    public void setData(LocalDate data) {
        this.data = data;
    }

    public String getDescricao() {
        return descricao;
    }

    public void setDescricao(String descricao) {
        this.descricao = descricao;
    }
}
