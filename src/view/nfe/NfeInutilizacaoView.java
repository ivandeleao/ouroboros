    private void confirmar() {
        
        int numeroInicial = Integer.valueOf(txtInicial.getText());
        
        int numeroFinal = Integer.valueOf(txtFinal.getText());
        
        int serie = Integer.valueOf(txtSerie.getText());
        
        String justificativa = txtMotivo.getText().trim();
        
        
        if((numeroInicial == 0 || numeroFinal == 0) || numeroFinal < numeroInicial) {
            JOptionPane.showMessageDialog(MAIN_VIEW, "Numeração inválida!", "Atenção", JOptionPane.WARNING_MESSAGE);
            txtInicial.requestFocus();
            
        } else if(serie == 0) {
            JOptionPane.showMessageDialog(MAIN_VIEW, "Série inválida!", "Atenção", JOptionPane.WARNING_MESSAGE);
            txtSerie.requestFocus();
            
        } else if(justificativa.length() < 15) {
            JOptionPane.showMessageDialog(MAIN_VIEW, "O motivo da inutilização deve ter no mínimo 15 caracteres!", "Atenção", JOptionPane.WARNING_MESSAGE);
            txtMotivo.requestFocus();
            
        } else {
            try {
                LocalDateTime dataHora = LocalDateTime.now();
                
                ConfiguracoesNfe config = NfeConfig.iniciarConfiguracoes();

                //Agora o evento pode aceitar uma lista de cancelamentos para envio em Lote.
                //Para isso Foi criado o Objeto Cancela
                //Evento inutilizacao = new Evento();
                
                //inutilizacao.setCnpj(Texto.soNumeros(Ouroboros.EMPRESA_CNPJ));
                
                //inutilizacao.setMotivo(txtMotivo.getText());
                
                //inutilizacao.setDataEvento(dataHora);
                
                
                // Monta o Evento
                TInutNFe inutNfe = InutilizacaoUtil.montaInutilizacao(
                        DocumentoEnum.NFE, Texto.soNumeros(Ouroboros.EMPRESA_CNPJ), serie, numeroInicial, numeroFinal, justificativa, dataHora, config);

                //Envia
                TRetInutNFe retorno = Nfe.inutilizacao(config, inutNfe, DocumentoEnum.NFE, true);
                
                //Valida o Retorno
                RetornoUtil.validaInutilizacao(retorno);
                

                //Resultado
                System.out.println();
                txtRetorno.append("# Status: " + retorno.getInfInut().getCStat() + " - " + retorno.getInfInut().getXMotivo());
                txtRetorno.append("# Protocolo: " + retorno.getInfInut().getNProt());

                //Cria ProcEvento da Inutilização
                String proc = InutilizacaoUtil.criaProcInutilizacao(config, inutNfe, retorno);
                
                txtRetorno.append("# ProcInutilizacao : " + proc);
                
                
                //Salvar xml
                /*  O nome do arquivo será composto por: 
                Ano de inutilização + CNPJ do emitente + Modelo + Série + Número Inicial + Número Final com extensão “-inu.xml”; */
                
                String xmlFileName = retorno.getInfInut().getAno()
                        + retorno.getInfInut().getCNPJ()
                        + retorno.getInfInut().getMod()
                        + retorno.getInfInut().getSerie()
                        + retorno.getInfInut().getNNFIni()
                        + retorno.getInfInut().getNNFFin()
                        + "-inu.xml";
                String pathXmlFile = NFE_PATH + "/enviados/" + xmlFileName;
                MwIOFile.writeFile(proc, pathXmlFile);
                
                

                
                JOptionPane.showMessageDialog(MAIN_VIEW, "Inutilização efetuada", "Inutilização efetuada", JOptionPane.INFORMATION_MESSAGE);
                
                //dispose();

            } catch (Exception e) {
                System.err.println();
                System.err.println(e.getMessage());
            }

        }
    }

