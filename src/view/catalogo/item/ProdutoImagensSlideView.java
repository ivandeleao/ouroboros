    private void definirAtalhos() {
        InputMap im = rootPane.getInputMap(WHEN_ANCESTOR_OF_FOCUSED_COMPONENT);
        ActionMap am = rootPane.getActionMap();

        im.put(KeyStroke.getKeyStroke(KeyEvent.VK_ESCAPE, 0), "exibirComandas");
        am.put("exibirComandas", new FormKeyStroke("ESC"));

    }

    protected class FormKeyStroke extends AbstractAction {

        private final String key;

        public FormKeyStroke(String key) {
            this.key = key;
        }

        @Override
        public void actionPerformed(ActionEvent e) {
            switch (key) {
                case "ESC":
                    dispose();
                    break;

            }
        }
    }

    private void carregarImagem(ProdutoImagem produtoImagem) {

        produtoImagemAtual = produtoImagem;
        
        txtDescricao.setText(produtoImagem.getDescricao());
        
        String caminhoPasta = Ouroboros.PRODUTO_IMAGENS_PATH + produto.getId() + "/";

        String caminhoArquivo = caminhoPasta + produtoImagem.getArquivo();
        System.out.println("path: " + caminhoArquivo);
        
        int largura = (int) lblImagem.getWidth() - 20;
        int altura = (int) lblImagem.getHeight() - 20;

        try {
            BufferedImage img = ImageIO.read(new File(caminhoArquivo));

            Double imgLargura = Double.valueOf(img.getWidth());
            Double imgAltura = Double.valueOf(img.getHeight());

            System.out.println("imgLargura: " + imgLargura);
            System.out.println("imgAltura: " + imgAltura);

            Double proporcao = 1.0;
            if (imgLargura > largura) {
                proporcao = imgLargura / largura;
                System.out.println("largura");
            }
            
            int novaLargura = (int) (imgLargura / proporcao);
            int novaAltura = (int) (imgAltura / proporcao);
            
            if (novaAltura > altura) {
                proporcao = imgAltura / altura;
                System.out.println("altura");
                
                novaLargura = (int) (imgLargura / proporcao);
                novaAltura = (int) (imgAltura / proporcao);
            }
            
            

            System.out.println("proporcao: " + proporcao);

            

            Image dimg = img.getScaledInstance(novaLargura, novaAltura, Image.SCALE_FAST);

            ImageIcon imageIcon = new ImageIcon(dimg);

            lblImagem.setIcon(imageIcon);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(MAIN_VIEW, "Erro ao carregar imagem", "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    private void salvar() {
        produtoImagemAtual.setDescricao(txtDescricao.getText());
        produtoImagemDAO.save(produtoImagemAtual);
    }
    
    private void voltar() {
        int index = produto.getProdutoImagens().indexOf(produtoImagemAtual);
        
        if (index > 0) {
            carregarImagem(produto.getProdutoImagens().get(index - 1));
        }
        
    }
    
    private void avancar() {
        int index = produto.getProdutoImagens().indexOf(produtoImagemAtual);
        
        if (index < produto.getProdutoImagens().size() - 1) {
            carregarImagem(produto.getProdutoImagens().get(index + 1));
        }
        
    }

