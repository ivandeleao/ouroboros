    private void txtBuscaRapidaKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtBuscaRapidaKeyReleased
        int index;
        switch (evt.getKeyCode()) {
            case KeyEvent.VK_ENTER:
                editar();
                break;
            case KeyEvent.VK_DOWN:
                index = tblTabelaPreco.getSelectedRow() + 1;
                if (index < tblTabelaPreco.getRowCount()) {
                    tblTabelaPreco.setRowSelectionInterval(index, index);
                    tblTabelaPreco.scrollRectToVisible(tblTabelaPreco.getCellRect(index, 0, true));
                }
                break;
            case KeyEvent.VK_UP:
                index = tblTabelaPreco.getSelectedRow() - 1;
                if (index > -1) {
                    tblTabelaPreco.setRowSelectionInterval(index, index);
                    tblTabelaPreco.scrollRectToVisible(tblTabelaPreco.getCellRect(index, 0, true));
                }
                break;
            case KeyEvent.VK_PAGE_DOWN:
                index = tblTabelaPreco.getSelectedRow() + 10;
                if (index > tblTabelaPreco.getRowCount() -1) {
                    index = tblTabelaPreco.getRowCount() -1;
                }
                tblTabelaPreco.setRowSelectionInterval(index, index);
                tblTabelaPreco.scrollRectToVisible(tblTabelaPreco.getCellRect(index, 0, true));
                break;
            case KeyEvent.VK_PAGE_UP:
                index = tblTabelaPreco.getSelectedRow() - 10;
                if (index < 0) {
                    index = 0;
                }
                tblTabelaPreco.setRowSelectionInterval(index, index);
                tblTabelaPreco.scrollRectToVisible(tblTabelaPreco.getCellRect(index, 0, true));
                break;
            default:
                carregarTabela();
        }
    }//GEN-LAST:event_txtBuscaRapidaKeyReleased

