    private void definirAtalhos() {
        InputMap im = getInputMap(WHEN_ANCESTOR_OF_FOCUSED_COMPONENT);
        ActionMap am = getActionMap();
        
        im.put(KeyStroke.getKeyStroke(KeyEvent.VK_F1, 0), "novo");
        am.put("novo", new FormKeyStroke("F1"));
        
    }
    
    protected class FormKeyStroke extends AbstractAction {

        private final String key;

        public FormKeyStroke(String key) {
            this.key = key;
        }

        @Override
        public void actionPerformed(ActionEvent e) {
            switch (key) {
                case "F1":
                    novo();
                    break;
                
            }
        }
    }
    
    private void formatarTabela() {
        tblTabelaPreco.setModel(tabelaPrecoJTableModel);

        tblTabelaPreco.setRowHeight(30);
        tblTabelaPreco.setIntercellSpacing(new Dimension(10, 10));
        
        
        tblTabelaPreco.getColumn("Id").setPreferredWidth(60);
        tblTabelaPreco.getColumn("Id").setCellRenderer(CELL_RENDERER_ALIGN_RIGHT);
        
        tblTabelaPreco.getColumn("Nome").setPreferredWidth(600);
        
        tblTabelaPreco.getColumn("Variação").setPreferredWidth(200);
        tblTabelaPreco.getColumn("Variação").setCellRenderer(new LineWrapCellRenderer());
    }
    
    private void novo() {
        new TabelaPrecoCadastroView(new TabelaPreco());
        carregarTabela();
    }
    
    private void editar() {
        TabelaPreco tabelaPreco = tabelaPrecoJTableModel.getRow(tblTabelaPreco.getSelectedRow());
        new TabelaPrecoCadastroView(tabelaPreco);
    }

    private void excluir() {
        int rowIndex = tblTabelaPreco.getSelectedRow();
        
        
        if(rowIndex >= 0) {
            TabelaPreco tabelaPreco = tabelaPrecoJTableModel.getRow(rowIndex);
            
            int resposta = JOptionPane.showConfirmDialog(MAIN_VIEW, "Excluir o item: " + tabelaPreco.getNome() + "?", "Atenção", JOptionPane.OK_CANCEL_OPTION, JOptionPane.WARNING_MESSAGE);

            if(resposta == JOptionPane.OK_OPTION) {
                tabelaPrecoDAO.delete(tabelaPreco);
                carregarTabela();
            }
        }
    }

    private void carregarTabela() {
        long start = System.currentTimeMillis();
        
        String buscaRapida = txtBuscaRapida.getText();
        
        tabelasPreco = tabelaPrecoDAO.findByCriteria(buscaRapida, false);
        
        tabelaPrecoJTableModel.clear();
        tabelaPrecoJTableModel.addList(tabelasPreco);

        long elapsed = System.currentTimeMillis() - start;
        lblMensagem.setText("Consulta realizada em " + elapsed + "ms");

        lblRegistrosExibidos.setText(String.valueOf(tabelasPreco.size()));
        
        //posicionar na primeira linha se não tiver linha selecionada
        if(tblTabelaPreco.getSelectedRow() == -1 && tblTabelaPreco.getRowCount() > 0) {
            tblTabelaPreco.setRowSelectionInterval(0, 0);
            tblTabelaPreco.scrollRectToVisible(tblTabelaPreco.getCellRect(0, 0, true));
        }
    }
    
    
    
    
