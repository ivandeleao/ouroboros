    private void carregarDados() {
        txtProduto.setText(produtoTamanho.getProduto().getNome());
        txtTamanho.setText(produtoTamanho.getTamanho().getNome());
        
        txtValor.setText(Decimal.toString(produtoTamanho.getValorVenda()));
        
    }
    

    
    private boolean salvar() {
        produtoTamanho.setValorVenda(Decimal.fromString(txtValor.getText()));
        
        produtoTamanhoDAO.save(produtoTamanho);
        return true;
    }
    
    private void confirmar() {
        if(salvar()) {
            dispose();
        }
    }
    

