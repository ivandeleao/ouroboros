    private void carregarTabela() {
        String buscaRapida = txtBuscaRapida.getText();

        veiculos = veiculoDAO.findByPlacaOuModelo(buscaRapida);

        veiculoPesquisaJTableModel.clear();
        veiculoPesquisaJTableModel.addList(veiculos);
        
        if(tblVeiculo.getRowCount() > 0){
            tblVeiculo.setRowSelectionInterval(0, 0);
        }
    }
    
