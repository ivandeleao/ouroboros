    private void txtBuscaRapidaKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtBuscaRapidaKeyReleased
        int index;
        switch (evt.getKeyCode()) {
            case KeyEvent.VK_ESCAPE:
                veiculo = null;
                dispose();
                break;
            case KeyEvent.VK_ENTER:
                veiculo = veiculoPesquisaJTableModel.getRow(tblVeiculo.getSelectedRow());
                dispose();
                break;
            case KeyEvent.VK_DOWN:
                index = tblVeiculo.getSelectedRow() + 1;
                if (index < tblVeiculo.getRowCount()) {
                    tblVeiculo.setRowSelectionInterval(index, index);
                    tblVeiculo.scrollRectToVisible(tblVeiculo.getCellRect(index, 0, true));
                }
                break;
            case KeyEvent.VK_UP:
                index = tblVeiculo.getSelectedRow() - 1;
                if (index > -1) {
                    tblVeiculo.setRowSelectionInterval(index, index);
                    tblVeiculo.scrollRectToVisible(tblVeiculo.getCellRect(index, 0, true));
                }
                break;
            case KeyEvent.VK_PAGE_DOWN:
                index = tblVeiculo.getSelectedRow() + 10;
                if (index > tblVeiculo.getRowCount() -1) {
                    index = tblVeiculo.getRowCount() -1;
                }
                tblVeiculo.setRowSelectionInterval(index, index);
                tblVeiculo.scrollRectToVisible(tblVeiculo.getCellRect(index, 0, true));
                break;
            case KeyEvent.VK_PAGE_UP:
                index = tblVeiculo.getSelectedRow() - 10;
                if (index < 0) {
                    index = 0;
                }
                tblVeiculo.setRowSelectionInterval(index, index);
                tblVeiculo.scrollRectToVisible(tblVeiculo.getCellRect(index, 0, true));
                break;
            default:
                carregarTabela();
        }
    }//GEN-LAST:event_txtBuscaRapidaKeyReleased

